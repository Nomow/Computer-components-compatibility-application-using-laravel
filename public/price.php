function float_rand($Min, $Max, $round)
{
    //validate input
    if ($Min > $Max) {$min = $Max;
        $max = $Min;} else { $min = $Min;
        $max = $Max;}
    $randomfloat = $Min + mt_rand() / mt_getrandmax() * ($max - $min);
    if ($round > 0) {
        $randomfloat = number_format(round($randomfloat, $round), 2);
    }

    return $randomfloat;
}

